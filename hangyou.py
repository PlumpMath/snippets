def html_detag(string):
    """Remove all HTML tags from the given STRING"""
    re_tag = re.compile("<[^>]*>")
    
    string = string.replace("<br>", "\n", -1)
    while True:
        match = re_tag.search(string)
        if match == None:
            break
        string = string.replace(match.group(), "")

    string = string.replace("&nbsp;", " ", -1)
    return string

