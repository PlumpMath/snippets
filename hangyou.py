    word_id = (word_id + WORD_ID_MAX + 1) % (WORD_ID_MAX + 1)
    url = "http://eedic.naver.com/eedic.naver?id=%u" % word_id
    
    re_def = re.compile("(^[0-9][0-9]*\..*$|^[ ]*[A-Z-]+[ ]*)")
    re_word = re.compile("""<span class="hwme2">([^<]*)</span>""")
    
    line = None
