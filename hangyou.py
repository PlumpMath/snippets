def eligible_word(word):
    """Return True iff the WORD is usable in the game."""
    # WORD contains non-ASCII character such as "café"
    # is not supported
    for ch in word:
        if not curses.ascii.isalpha(ch) and ch != " " and ch != "-":
            return False
    return True

